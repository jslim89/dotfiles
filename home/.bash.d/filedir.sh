# directory
alias ..='cd ..'
alias ...='cd ../..'
alias ....='cd ../../..'

# file listing
alias ls='ls --color --group-directories-first -p';
alias ll='ls -lh --color';
alias lsa='ls -ah';
alias lla='ls -alh';
alias lf='ls -l | grep "^d"';
alias lfa='ls -la | grep "^d"';
alias lfo='ls -la | grep "^-"';
